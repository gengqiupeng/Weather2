package com.beiliji.weather.bean;

public class WeatherBean
{
  private String date;
  private String tem_down;
  private String tem_hig;
  private String weather_day;
  private String weather_nig;

  public String getDate() {
    return date;
  }

  public void setDate(String date) {
    this.date = date;
  }

  public String getTem_down() {
    return tem_down;
  }

  public void setTem_down(String tem_down) {
    this.tem_down = tem_down;
  }

  public String getTem_hig() {
    return tem_hig;
  }

  public void setTem_hig(String tem_hig) {
    this.tem_hig = tem_hig;
  }

  public String getWeather_day() {
    return weather_day;
  }

  public void setWeather_day(String weather_day) {
    this.weather_day = weather_day;
  }

  public String getWeather_nig() {
    return weather_nig;
  }

  public void setWeather_nig(String weather_nig) {
    this.weather_nig = weather_nig;
  }
}